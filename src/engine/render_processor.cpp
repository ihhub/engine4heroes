#include "render_processor.h"

#include <cstdint>

namespace engine4heroes
{
    RenderProcessor & RenderProcessor::instance()
    {
        static RenderProcessor processor;
        return processor;
    }

    bool RenderProcessor::preRenderAction()
    {
        if ( _enableRenderers && _preRenderer ) {
            _preRenderer();
        }

        return true;
    }

    void RenderProcessor::postRenderAction() const
    {
        if ( _enableRenderers && _postRenderer ) {
            _postRenderer();
        }
    }
}
